export const imageLoadingCallback = (info, loading, message) => {
  if (info.file.status === 'uploading') {
    loading(true);
    return;
  }
  if (info.file.status === 'done') {
    loading(true);
    message.success('上传成功');
  } else {
    loading(false);
    message.error('上传失败');
  }
};

export const imageUploadCallback = (e, fn) => {
  if (e.file.response) {
    if (e.file.response.code === 1) {
      fn(e.file.response.data.imageUrl);
      return e.file.response.data.imageKey;
    }
    return null;
  }
  return null;
};

